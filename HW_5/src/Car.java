public class Car {

    public int id;
    public String brand;
    public String model;
    public int year;
    public String color;
    public int price;
    public String number;

    public Car(int id, String brand, String model, int year, String color, int price, String number) {
        this.id = id;
        this.brand = brand;
        this.model = model;
        this.year = year;
        this.color = color;
        this.price = price;
        this.number = number;
    }

}
